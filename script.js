function realizarAnimacion1() {
    var foto = document.getElementById('fotopython');
    var texto = document.getElementById('textopython');

    // Mostrar el texto después de un breve retraso
    setTimeout(function() {
        texto.style.opacity = 1;
    }, 500); // 500 milisegundos (0.5 segundos)
}

